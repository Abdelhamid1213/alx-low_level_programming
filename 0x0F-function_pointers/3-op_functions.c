/**
 * op_add - add two numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: result 
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: result 
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: result 
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: result 
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - return modulos of two numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: result 
 */

int op_mod(int a, int b)
{
	return (a % b);
}
